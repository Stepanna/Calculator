public enum Operation {;

    public static int MULTIPLY(int[] i) {
        return i[0] * i[1];
    }

    public static int DIVIDE(int[] i) {
        return i[0] / i[1];
    }

    public static int ADD(int[] i) {
        return i[0] + i[1];
    }

    public static int SUBTRACT(int[] i) {
        return i[0] - i[1];
    }
}
